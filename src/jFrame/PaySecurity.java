    public void security() {
        
        String pass = txt_pin.getText();
        ResultSet rs = null;

        try {
            Connection con = DbConnection.getConnection();
            PreparedStatement prt = con.prepareStatement("select * from users where name = '"+Name4.getText()+"' ");

            rs = (ResultSet) prt.executeQuery();

            if (rs.next()) {
                String password = rs.getString("password_hash");

                if (BCrypt.checkpw(pass, password)) {
                    FinePage page = new FinePage();
                    page.setVisible(true);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(this, "This pin invalid or empty");
            }
            rs.close();
            prt.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }

    }

