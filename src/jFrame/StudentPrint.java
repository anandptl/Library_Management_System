    public void searchByE() {
        String name = txt_sbn.getText();

        try {
            Connection con = DbConnection.getConnection();
            String sql = "select * from students where aadhaar = ?";
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setString(1, name);
//            pst.setString(2, Branch);

            ResultSet rs = pst.executeQuery();
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            clearTable();

            while (rs.next()) {
                String studentId = rs.getString("student_id");
                String studentName = rs.getString("student_name");
                String phone = rs.getString("phone_number");
                String Address = rs.getString("address");
                String aadhaar = rs.getString("aadhaar");
                String enrollDate = rs.getLong("enroll_date") > 0 ? sdf.format(new java.sql.Date(rs.getLong("enroll_date"))) : "N/A";
                String expiryDate = rs.getLong("expiry_date") > 0 ? sdf.format(new java.sql.Date(rs.getLong("expiry_date"))) : "N/A";
                String ibn = rs.getString("issue_book_no");

                Object[] obj = {studentId, studentName, phone, Address, aadhaar, enrollDate, expiryDate, ibn};
                model = (DefaultTableModel) table_StudentDetails.getModel();
                model.addRow(obj);
            }
            rs.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

