    private void jBtnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPrintActionPerformed
        try {
            // Create a JFileChooser to allow the user to select where to save the image
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.setDialogTitle("Save Image As");
            fileChooser.setApproveButtonText("Save");
            fileChooser.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
            fileChooser.setAcceptAllFileFilterUsed(false); // Disables the "All files" option to avoid confusion

            // Set default file extension to .jpg
            fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("JPG Image", "jpg"));

            // Show the Save dialog
            int userSelection = fileChooser.showSaveDialog(null);
            
            if (userSelection == JFileChooser.APPROVE_OPTION) {
                // Get the selected file path
                File fileToSave = fileChooser.getSelectedFile();
                
                // Check if the file doesn't already have a .jpg extension
                String filePath = fileToSave.getAbsolutePath();
                if (!filePath.toLowerCase().endsWith(".jpg")) {
                    filePath += ".jpg";  // Add .jpg extension if not present
                }

                // Create the BufferedImage (Assuming id_panel is your JPanel or any component to capture image from)
                BufferedImage image = new BufferedImage(id_panel.getWidth(), id_panel.getHeight(), BufferedImage.TYPE_INT_RGB);
                Graphics2D g2d = image.createGraphics();
                id_panel.paint(g2d);
                g2d.dispose();

                // Save the image in JPG format to the selected location
                ImageIO.write(image, "jpg", new File(filePath));

                // Show confirmation message
                JOptionPane.showMessageDialog(null, "Image saved successfully: " + filePath);
            } else {
                JOptionPane.showMessageDialog(null, "No file selected. Image not saved.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error saving image: " + e.getMessage());
        }
    }//GEN-LAST:event_jBtnPrintActionPerformed

